function VGT:UnitNameFromGuid(creatureGuid)
    if not creatureGuid then
        return "Unknown"
    end
    local unitType, _, _, _, _, unitId, spawnUID = strsplit("-", creatureGuid)
    unitId = tonumber(unitId)
    return VGT._creatures[unitId] or ("Unknown " .. (unitType or "") .. " " .. (unitId or ""))
end

local function addCreature(id, name)
    VGT._creatures[id] = name
end

